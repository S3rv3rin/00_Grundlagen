public class ErsteSpielereien {
    public static void main(String[] args) {

        // eine Variable deklarieren:
        // Datentyp + Bezeichner
        // Datentyp:    boolean
        // (primitive)  byte, short, int, long, float, double
        //              char
        // (komplexe)   String
        //              array
        int meineVariable;

        // dieser Variablen einen Wert zuweisen
        meineVariable = 10;

        // oder direkt Deklaration + Initialisierung
        int eineAndereVariable = 17;

        System.out.println("Wert der Variablen\t" + meineVariable);

        // Rechenoperation
        meineVariable = meineVariable + eineAndereVariable;

        // der Wert der Variablen ausgeben
        System.out.println(meineVariable);

    }
}
